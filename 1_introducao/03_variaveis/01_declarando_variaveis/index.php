<?php
    
    // PHP declara variáveis usando o símbolo $
    // Esse símbolo agiliza o processo de reconhecimento
    // de uma variável por parte do computador
    $x = "Minha primeira variável em PHP";
    echo $x;

    echo "<br><br>";

    // É possível atribuir o valor de variáveis
    // em outras variáveis
    $y = 1;
    $z = $y;
    echo '$y = ' . $y . '<br>';
    echo '$z = ' . $z . '<br>';

