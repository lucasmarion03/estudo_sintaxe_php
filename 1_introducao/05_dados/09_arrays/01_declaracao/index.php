<?php
    # Maneiras de criar um array

    # Através de colchetes
    $carros = ["Volvo", "BMW", "Toyota"];
    # Através da função array()
    $carros = array("Volvo", "BMW", "Toyota");
    # Múltiplas linhas
    $carros = [
        "Volvo", 
        "BMW", 
        "Toyota"
    ];

    $carros = array(
        "Volvo",
        "BMW",
        "Toyota"
    );
    # Criando um array vazio e adicionando elementos
    $carros = [];
    $carros[0] = "Volvo";
    $carros[1] = "BMW";
    $carros[2] = "Toyota";